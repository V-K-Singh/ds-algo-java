package com.workshop.ds.linkedlist;

public class LinkedListNode {
	int data;
	LinkedListNode next;
	public LinkedListNode(int data){
		this.data = data;
	}
	public int getData() {
		return data;
	}
	public void setData(int data) {
		this.data = data;
	}
	public LinkedListNode getNext() {
		return next;
	}
	public void setNext(LinkedListNode next) {
		this.next = next;
	}
	
	
}
